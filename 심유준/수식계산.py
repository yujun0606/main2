#연산자(+, -, *, 나누기 : / , // , % , **)

'''
1) 나누기 / : 소수점을 보여줌, // : 소수점을 없애줌
ex)

2) 나머지 : %

3) 제곱 : **

'''
#수식 계산 프로그램
print("7 + 3 =" , 7 + 3)

print(f"7 + 3 = {7 + 3}")

#포멧팅 사용시 항상 앞에 f로 시작, 문자로 보여주고 싶은 부분 따옴표
#!!! 값을 보여주고 싶은 부분은 {}로 감싸자

print(f"7 + 3 = {7 + 3}")
print(f"7 - 3 = {7 - 3}")
print(f"7 / 3 = {7 / 3}")
print(f"7 // 3 = {7 // 3}")
print(f"7 % 3 = {7 % 3}")
print(f"7 ** 3 = {7 ** 3}")
