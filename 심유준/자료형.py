# 자료형(정수, 실수, 문자열, 논리형)
#논리형 = 부울형 (boolean) -> True, False

# type() : 어떤 자료형인지 알고 싶을 때

#int = 정수, bool = 논리형, float = 실수형, str = 문자

print(type(3))
print(type(True))
print(type(3.14))
print(type("python"))

# 타입변환
print(int(3.14))
